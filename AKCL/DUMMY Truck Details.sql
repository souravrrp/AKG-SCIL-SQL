         MODT.DO_NUMBER,
         MOH.VEHICLE_NO,
         MOH.MOV_ORDER_NO,
         MOH.MOV_ORDER_STATUS,
         MOH.WAREHOUSE_ORG_CODE,
         MOH.FINAL_DESTINATION,
         MOH.TRANSPORT_MODE,
         MOH.TRANSPORTER_NAME,
         MOH.INITIAL_GATE_IN,
         MOH.GATE_OUT,
         MOH.GATE_IN,
         MOH.READY_FOR_INVOICE,
         --MOH.GATE_PASS_NO,

  SELECT MODT.CUSTOMER_NUMBER,
         (SELECT HP.PARTY_NAME
            FROM APPS.HZ_PARTIES HP, APPS.HZ_CUST_ACCOUNTS HC
           WHERE     HP.PARTY_ID = HC.PARTY_ID
                 AND HC.ACCOUNT_NUMBER = MODT.CUSTOMER_NUMBER)
            CUSTOMER_NAME,
         MODT.DO_NUMBER,
         SUM (DODL.LINE_QUANTITY) DO_QUANTITY,
         MOH.VEHICLE_NO,

         TO_CHAR (MOH.CONFIRMED_DATE) CONFIRMED_DATE
    FROM APPS.XXAKG_MOV_ORD_HDR MOH,
         APPS.XXAKG_MOV_ORD_DTL MODT,
         APPS.XXAKG_DEL_ORD_DO_LINES DODL
   WHERE     1 = 1

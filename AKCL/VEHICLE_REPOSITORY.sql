APPS.PQP_VEHICLE_REPOSITORY_F VR
WHERE   1=1
AND SYSDATE BETWEEN VR.EFFECTIVE_START_DATE AND VR.EFFECTIVE_END_DATE
AND  REGISTRATION_NUMBER = 'D.M.AU-11-4261'
AND VRE_ATTRIBUTE2 = 82
--AND VRE_ATTRIBUTE3 = 'Road Transport'
AND VEHICLE_STATUS = 'A'


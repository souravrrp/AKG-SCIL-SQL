SELECT     
Registration_Number VEHICLE_NO
,INSURANCE_EXPIRY_DATE "Insurance Expiration Date"
,VRE_ATTRIBUTE16 "Vehicle Group"
,NVL(LOCATION_NAME,'SCIL Factory') "Operating Location"
,VRE_ATTRIBUTE12 "Tax Token Expire Date"
,VRE_ATTRIBUTE14 "Route Permit Expire Date"
,VRE_ATTRIBUTE13 "Fitness Expire Date"
,DRIVER_NUMBER DRIVER_ID
,DRIVER_FULL_NAME DRIVER_NAME
,VR.*
FROM
APPS.XXAKG_VEHICLE_REPOSITORY_F VR
WHERE 1=1
AND VR.VRE_ATTRIBUTE2 = 82
AND VR.VEHICLE_STATUS='A'
AND UPPER (REGISTRATION_NUMBER) LIKE UPPER ('%' || :P_VEHICLE_NUMBER || '%')
--AND VR.VRE_ATTRIBUTE3='Road Transport'
AND SYSDATE BETWEEN EFFECTIVE_START_DATE AND EFFECTIVE_END_DATE
--AND TRUNC(VR.CREATION_DATE) BETWEEN '28-NOV-2018' and '29-NOV-2018'

